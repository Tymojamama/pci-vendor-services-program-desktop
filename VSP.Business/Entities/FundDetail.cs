        public static FundDetail GetAssociatedFundDetail(Fund fund, TimeTable timeTable)
        {
            if (fund == null || timeTable == null)
            {
                return null;
            }

            DataTable dataTable = GetAssociatedFromFundAndTimeTable(fund.Id, timeTable.Id);

            if (dataTable.Rows == null || dataTable.Rows.Count == 0)
            {
                return null;
            }

            DataRow dataRow = dataTable.Rows[0];
            Guid fundDetailId = new Guid(dataRow["FundDetailId"].ToString());
            FundDetail fundDetail = new FundDetail(fundDetailId);
            return fundDetail;
        }

        private static DataTable GetAssociatedFromFundAndTimeTable(Guid fundId, Guid timeTableId)
        {
            Hashtable parameterList = new Hashtable();
            parameterList.Add("@FundId", fundId);
            parameterList.Add("@TimeTableId", timeTableId);
            return Access.VspDbAccess.ExecuteStoredProcedureQuery("[dbo].[usp_ISP_FundDetailGetAssociatedFromFundAndTimeTable]", parameterList);
        }
